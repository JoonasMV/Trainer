package com.example.trainer.schemas;

public class ExerciseType {
    private String name;

    private int id;

    public ExerciseType(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
